function buildActivityDemoResponse(query) {
    var keyword = query && query.search ? $.trim(query.search).toLowerCase() : '';
    var rows = activityDemoRows.filter(function (row) {
        if (!keyword) {
            return true;
        }
        return Object.keys(row).some(function (key) {
            return row[key] != null && row[key].toString().toLowerCase().indexOf(keyword) !== -1;
        });
    });
    var offset = parseInt(query && query.offset, 10);
    var limit = parseInt(query && query.limit, 10);
    offset = isNaN(offset) || offset < 0 ? 0 : offset;
    limit = isNaN(limit) || limit <= 0 ? rows.length : limit;
    return {
        code: 0,
        total: rows.length,
        rows: rows.slice(offset, offset + limit)
    };
}

function resolveActivityListResponse(res, query) {
    if (res && res.rows && res.rows.length > 0) {
        return res;
    }
    return buildActivityDemoResponse(query);
}

function formatActivityTime(value) {
    if (!value) {
        return '';
    }
    if (typeof formatDate === 'function') {
        return formatDate(value);
    }
    var date = value instanceof Date ? value : new Date(value);
    if (isNaN(date.getTime())) {
        return value;
    }
    var pad = function (num) {
        return num < 10 ? '0' + num : num;
    };
    return date.getFullYear() + '-' +
        pad(date.getMonth() + 1) + '-' +
        pad(date.getDate()) + ' ' +
        pad(date.getHours()) + ':' +
        pad(date.getMinutes());
}

