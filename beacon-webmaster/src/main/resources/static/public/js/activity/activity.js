var vm = new Vue({
    el: '#dtapp',
    data: {
        showList: true,
        title: null,
        activity: {}
    },
    methods: {
        del: function () {
            var rows = getSelectedRows();
            if (rows == null) {
                return;
            }
            var id = 'id';
            //提示确认框
            layer.confirm('您确定要删除所选数据吗？', {
                btn: ['确定', '取消'] //可以无限个按钮
            }, function (index, layero) {
                var ids = new Array();
                //遍历所有选择的行数据，取每条数据对应的ID
                $.each(rows, function (i, row) {
                    ids[i] = row[id];
                });

                $.ajax({
                    type: "POST",
                    url: "/sys/activity/del",
                    data: JSON.stringify(ids),
                    success: function (r) {
                        if (r.code === 0) {
                            layer.alert('删除成功');
                            $('#table').bootstrapTable('refresh');
                        } else {
                            layer.alert(r.msg);
                        }
                    },
                    error: function () {
                        layer.alert('服务器没有返回数据，可能服务器忙，请重试');
                    }
                });
            });
        },
        add: function () {
            vm.showList = false;
            vm.title = "新增";
            vm.activity = {parentName: null, parentId: 0, type: 1, orderNum: 0};
        },
        update: function (event) {
            var id = 'id';
            var id = getSelectedRow()[id];
            if (id == null) {
                return;
            }

            $.get("../sys/activity/info/" + id, function (r) {
                vm.showList = false;
                vm.title = "修改";
                vm.activity = r.activity;
            });
        },
        saveOrUpdate: function (event) {
            var url = vm.activity.id == null ? "../sys/activity/save" : "../sys/activity/update";
            $.ajax({
                type: "POST",
                url: url,
                data: JSON.stringify(vm.activity),
                success: function (r) {
                    if (r.code === 0) {
                        layer.alert(r.msg, function (index) {
                            layer.close(index);
                            vm.reload();
                        });
                    } else {
                        layer.alert(r.msg);
                    }
                }
            });
        },
        handleSuccess: function (res) {
            if (res.code == 0) {
                vm.activity.coverPic = res.file;
                //    console.log("ooooooooo"+vm.article.coverPic);
            }
        },
        reload: function (event) {
            vm.showList = true;
            $("#table").bootstrapTable('refresh');
        }
    }
});
